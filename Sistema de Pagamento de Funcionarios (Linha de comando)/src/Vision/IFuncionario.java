    private void salvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButtonActionPerformed
        if (rgCaixa1.getText() == null || nomeCaixa1.getText() == null || cpfCaixa1.getText() == null
                || tituloCaixa1.getText() == null || telefoneFixoCaixa1.getText() == null || telefoneCelularCaixa1.getText() == null
                || telefoneCelularCaixa1.getText() == null || telefoneCelularCaixa1.getText() == null || resevistaCaixa1.getText() == null
                || dataNacimentoCaixa1.getText() == null || campoDependentes1.getText() == null || ("" + estadoCivilCampo1.getSelectedItem()).equalsIgnoreCase("<  Estado Cívil >")
                || caixaEmail1.getText() == null || ("" + statusCampo1.getSelectedItem()).equalsIgnoreCase("<  Status >")
                || dataAdimissaoCampo1.getText() == null) {
            JOptionPane.showMessageDialog(null, "Favor preencha todos os campos");

        } else {
            deletar();
            DefaultTableModel funcionario = (DefaultTableModel) tabelaFuncionario.getModel();
            funcionario.addRow(new String[]{nomeCaixa1.getText(), rgCaixa1.getText(), cpfCaixa1.getText(), tituloCaixa1.getText(),
                caixaEmail1.getText(), telefoneFixoCaixa1.getText(), telefoneCelularCaixa1.getText(), resevistaCaixa1.getText(), dataNacimentoCaixa1.getText(),
                dataAdimissaoCampo1.getText(), campoDependentes1.getText(), estadoCivilCampo1.getSelectedItem() + "", idCaixa1.getText(), statusCampo1.getSelectedItem() + ""});
            try {

                escreverArquivo.salvar(funcionario, local);
                JOptionPane.showMessageDialog(null, "Funcionario Editado Com Sucesso");
                limpar();
                FuncionarioMenu.setSelectedIndex(0);
                bloquear();

            } catch (IOException ex) {
                Logger.getLogger(IFuncionario.class
                        .getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_salvarButtonActionPerformed

