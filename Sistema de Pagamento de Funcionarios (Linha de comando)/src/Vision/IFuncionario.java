    private void editarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarButtonActionPerformed
        if (tabelaFuncionario.getSelectedRow() >= 0) {
            FuncionarioMenu.setEnabledAt(0, false);
            FuncionarioMenu.setSelectedIndex(2);
            int aux = tabelaFuncionario.getSelectedRow();
            nomeCaixa1.setText((String) tabelaFuncionario.getValueAt(aux, 0));
            rgCaixa1.setText((String) tabelaFuncionario.getValueAt(aux, 1));
            cpfCaixa1.setText((String) tabelaFuncionario.getValueAt(aux, 2));
            tituloCaixa1.setText((String) tabelaFuncionario.getValueAt(aux, 3));
            caixaEmail1.setText((String) tabelaFuncionario.getValueAt(aux, 4));
            telefoneFixoCaixa1.setText((String) tabelaFuncionario.getValueAt(aux, 5));
            telefoneCelularCaixa1.setText((String) tabelaFuncionario.getValueAt(aux, 6));
            resevistaCaixa1.setText((String) tabelaFuncionario.getValueAt(aux, 7));
            dataNacimentoCaixa1.setText((String) tabelaFuncionario.getValueAt(aux, 8));
            dataAdimissaoCampo1.setText((String) tabelaFuncionario.getValueAt(aux, 9));
            campoDependentes1.setText((String) tabelaFuncionario.getValueAt(aux, 10));
            String estado = (String) tabelaFuncionario.getValueAt(aux, 11);
            if (estado.equalsIgnoreCase("solteiro")) {
                estadoCivilCampo1.setSelectedIndex(1);
            } else {
                estadoCivilCampo1.setSelectedIndex(2);
            }

            idCaixa1.setText((String) tabelaFuncionario.getValueAt(aux, 12));
            String status = (String) tabelaFuncionario.getValueAt(aux, 13);
            if (status.equalsIgnoreCase("Ativo")) {
                statusCampo1.setSelectedIndex(1);
            } else {
                if (status.equalsIgnoreCase("Inativo")) {
                    statusCampo1.setSelectedIndex(2);
                } else {
                    statusCampo1.setSelectedIndex(3);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Favor selecionar uma linha");

        }

    }//GEN-LAST:event_editarButtonActionPerformed
    void filtrarFuncionario() {
        TableRowSorter sorter = null;
        DefaultTableModel funcionarios = (DefaultTableModel) tabelaFuncionario.getModel();
        sorter = new TableRowSorter(funcionarios);
        tabelaFuncionario.setRowSorter(sorter);
        String text = pesquisaCaixaFuncionario.getText();
        if (text.length() == 0) {
            sorter.setRowFilter(null);
        } else {
            try {
                sorter.setRowFilter(RowFilter.regexFilter(text));
            } catch (Exception ex) {
            }
        }
    }
